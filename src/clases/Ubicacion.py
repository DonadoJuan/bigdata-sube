class Ubicacion:

    def __init__(self, identificador, nombre, tipo, calle, numero, barrio, comuna, partido, localidad, provincia, codigopostal):
        self.id = identificador
        self.nombre = nombre
        self.tipo = tipo
        self.calle = calle
        self.numero = numero
        self.barrio = barrio
        self.comuna = comuna
        self.partido = partido
        self.localidad = localidad
        self.provincia = provincia
        self.codigopostal = codigopostal
